/* comparators are JS operators, they compare 2 values and return the boolean result TRUE or FALSE, put 1 value on the leftside, the other on the rightside and add a comparator between them */

/* comparators: 
< is less than, 
< = is less than or equal to, 
== is equal to, 
> = is greater than or equal to, 
> is greater than. */

let a = 10;
let b = 20;

console.log(a<b); // , a is less than b
console.log(a<=b); // a is lass than or equal to b  
console.log(a==b); // a is equal to b 
console.log(a>=b); // a is greater than or equal to b 
console.log(a>b); // a is greater than b 